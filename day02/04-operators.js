//Increment Operator - ++
let value = 10;

//Post increment - value++
//value = value + 1
console.log(value++);
console.log(value);

//Pre Increment
console.log(++value);
console.log(value);

//Decrement Operator
let result = 20;
//Post decrement
console.log(result--);
console.log(result);

//Pre decrement
console.log(--result);

/**
 * Strict equality - 1. Compare the data type 2. Compare the value
 * Non-strict equality - Compares only the values and not the data type
 */

console.log(1===1);     //true
console.log(1===5);     //false
console.log(1=="1");    //true
console.log("chrome"==="Chrome");   //false
console.log(1==true);   //true
console.log(true=="true");  //false






